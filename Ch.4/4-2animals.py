for animal in animals:
    print(animal.title())
    print("A", animal.title() + ", would not make a great pet, they are far to dangerous, but one can dream\n")
print("All of the following:\n\t" + animals[0].title(), "\n\t" + animals[1].title(), "\n\t" + animals[2].title(), "\nare big cats and very powerful.")
