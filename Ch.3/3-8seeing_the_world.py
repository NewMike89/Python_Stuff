# printing a list and sorting it in several different ways.

places = ['italy', 'germany', 'canada', 'philippines', 'mexico']
print(places)
print(sorted(places))
print(places)
print(sorted(places, reverse=True))
print(places)
places.reverse()
print(places)
places.reverse()
print(places)
places.sort()
print(places)
places.sort(reverse=True)
print(places)
